<h2 class="text-color">Payment by card on the website<br>
 </h2>
<p>
    <b> Order payment online, Visa or MasterCard. </b>
</p>
<p>
    Delivery of the goods is possible only after confirmation of payment. You can pay for the order online, by Visa or MasterCard.
</p>
Payment by payment cards is made in the following way:
<ul class = "pl-4">
    <li> When ordering on the site, you will be asked to choose a payment method. </li>
    <li> In the "Payment" column, you need to select "Visa / MasterCard payment". </li>
    <li> You will then be redirected to the bank's secure payment system page, where you will need to confirm your payment. </li>
</ul>

<strong> Please note. </strong> <br>
<br>
Only the customer whose name was indicated on the website at the time of application can receive the goods paid for with a payment card, so you must have a passport when receiving the order.
